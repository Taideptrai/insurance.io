/**
 * Get color from stage value
 * @param  {String} stage
 */
export function getColorFromStage(stage) {
  let color = 'dark-grey-1'
  switch (stage) {
    case 'quote_pack':
      color = 'warning'
      break
    case 'binder_pack':
      color = 'accent'
      break
    case 'signing_and_completion':
      color = 'success'
      break
  }
  return color
}
